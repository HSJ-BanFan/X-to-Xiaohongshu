CONTENT_CLASSIFIER_PROMPT = """
你是一个内容分析助手。你需要分析下面这篇来自社交媒体（如 Twitter）的帖子，
判断它如果要发到小红书，最适合哪一种内容分类排版风格。

可选的分类（类别标识）：
1. "TUTORIAL" - 教程/干货/知识科普（包含：步骤、代码、配置说明、技术分享、菜谱、攻略等）
2. "REVIEW"   - 测评/推荐/避坑（包含：好物分享、优缺点点评、买后感、软件工具推荐等）
3. "NEWS"     - 新闻/资讯/吃瓜（包含：科技热点、发布会、公司新闻、八卦、行业变动等）
4. "LIFESTYLE"- 生活分享/情感碎碎念（包含：个人感悟、心情、日常照片、牢骚、风景、萌宠等，没有明确干货的日常）

推文内容：
\"\"\"
{content}
\"\"\"
附带图片/视频数量：{media_count}

【输出格式】纯 JSON，只需输出一个字段 "category"，值为上述 4 个标识之一。
如果有疑惑，优先选择 "LIFESTYLE"。如果像技术分享/方法论，选 "TUTORIAL"。
示例：{{"category": "LIFESTYLE"}}
"""

# ==========================================
# 获取生成的最终 Prompt
# ==========================================
def get_classification_prompt(text: str, media_count: int) -> str:
    return CONTENT_CLASSIFIER_PROMPT.format(content=text[:1000], media_count=media_count)

def get_generation_prompt(content_type: str, text: str) -> str:
    """根据分类获取完整的主生成 Prompt"""
    template = TEMPLATES.get(content_type, TEMPLATES["DEFAULT"])
    return template.format(content=text, core_rules=XHS_CORE_RULES)

